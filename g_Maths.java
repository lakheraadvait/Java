public class g_Maths {
    public static void main(String[] args) {
        double result;
        result = Math.PI; // Provides the value of Pi
        result = Math.E; // Provides the value of E
        result = Math.abs(-5); // Provides positive number from a negative one
        result = Math.pow(2, 3); // Provides exponent with first number as base and second as power
        result = Math.sqrt(9); // Provides square root
        result = Math.floor(3.7); // Rounds to lower bound
        result = Math.ceil(3.1); // Rounds to higher bound
        result = Math.round(3.2343); // Rounds to appropriate bound
        result = Math.max(10, 20); // Returns maximum value
        result = Math.min(1, 53); // Returns minimum value
        result = Math.cos(30); // Returns trigonometric values like cos or sin or tan
        System.out.println(result);
    }
}
